#!/usr/bin/env python3
"""Run real-artifact DPF round benchmarks and emit paper tables.

This benchmark uses the current Bitcoin PIR cuckoo artifacts and the runtime
DPF evaluation code (`pir-runtime-core::eval`). For each measured row it
generates fresh DPF key shares, evaluates both server shares locally, XORs the
responses, and verifies that every recovered result equals the selected real
cuckoo bin. The timing remains a local warm-cache benchmark, not a networked
deployment measurement.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import platform
import statistics
import subprocess
from datetime import datetime, timezone
from pathlib import Path


BENCH_DIR = Path("benchmarks/dpf_round_bench")
DEFAULT_OUT_DIR = Path("benchmarks/results")
DEFAULT_RUNTIME_REPO = Path("/Users/cusgadmin/BitcoinPIR")


def run_text(cmd: list[str], cwd: Path | None = None) -> str:
    try:
        return subprocess.check_output(cmd, cwd=cwd, text=True).strip()
    except Exception:
        return ""


def percentile(values: list[float], pct: float) -> float:
    ordered = sorted(values)
    if len(ordered) == 1:
        return ordered[0]
    rank = (len(ordered) - 1) * pct
    lo = int(rank)
    hi = min(lo + 1, len(ordered) - 1)
    frac = rank - lo
    return ordered[lo] * (1 - frac) + ordered[hi] * frac


def write_csv(path: Path, rows: list[dict[str, object]]) -> None:
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()), lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)


def summarize(rows: list[dict[str, str]]) -> list[dict[str, object]]:
    groups: dict[tuple[str, str], list[dict[str, str]]] = {}
    for row in rows:
        groups.setdefault((row["database"], row["level"]), []).append(row)

    out: list[dict[str, object]] = []
    for (database, level), group in groups.items():
        keygen = [float(r["keygen_s"]) for r in group]
        server = [float(r["server_parallel_s"]) for r in group]
        verify = [float(r["client_verify_s"]) for r in group]
        total = [float(r["total_parallel_s"]) for r in group]
        failures = sum(int(r["verification_failures"]) for r in group)
        out.append(
            {
                "database": database,
                "level": level,
                "trials": len(group),
                "k": group[0]["k"],
                "bins_per_table": group[0]["bins_per_table"],
                "dpf_n": group[0]["dpf_n"],
                "body_bytes": group[0]["body_bytes"],
                "result_size_bytes": group[0]["result_size_bytes"],
                "key_size_bytes": group[0]["key_size_bytes"],
                "upload_per_server_bytes": group[0]["upload_per_server_bytes"],
                "download_per_server_bytes": group[0]["download_per_server_bytes"],
                "verified_queries": sum(int(r["verified_queries"]) for r in group),
                "verification_failures": failures,
                "median_keygen_s": f"{statistics.median(keygen):.6f}",
                "median_server_parallel_s": f"{statistics.median(server):.6f}",
                "p95_server_parallel_s": f"{percentile(server, 0.95):.6f}",
                "median_client_verify_s": f"{statistics.median(verify):.6f}",
                "median_total_parallel_s": f"{statistics.median(total):.6f}",
                "p95_total_parallel_s": f"{percentile(total, 0.95):.6f}",
            }
        )
    return out


def human_body(n: int) -> str:
    if n >= 1024**3:
        return f"{n / 1024**3:.2f} GiB"
    return f"{n / 1024**2:.1f} MiB"


def write_tex(path: Path, rows: list[dict[str, object]]) -> None:
    lines = [
        "% Auto-generated by benchmarks/benchmark_dpf_round_latency.py.",
        "\\begin{table}[ht]",
        "\\centering",
        "\\small",
        "\\begin{tabular}{llrrrrrr}",
        "\\hline",
        "Database & Level & Body & Trials & Keygen & Server p50 & Server p95 & Total p50 \\\\",
        "\\hline",
    ]
    for r in rows:
        db = str(r["database"]).replace("_", "\\_")
        level = "INDEX" if r["level"] == "index" else "CHUNK"
        lines.append(
            f"{db} & {level} & {human_body(int(r['body_bytes']))} & {r['trials']} & "
            f"{float(r['median_keygen_s']):.3f} s & "
            f"{float(r['median_server_parallel_s']):.3f} s & "
            f"{float(r['p95_server_parallel_s']):.3f} s & "
            f"{float(r['median_total_parallel_s']):.3f} s \\\\"
        )
    lines += [
        "\\hline",
        "\\end{tabular}",
        "\\caption{Local warm-cache DPF round benchmark over current real cuckoo artifacts. Each trial generates DPF key shares, evaluates both local server shares, XORs responses, and verifies selected bins. Server latency is reported as the slower of the two local server-share evaluations, matching the two-server parallel critical path without network latency.}",
        "\\label{tab:dpf-round-benchmark}",
        "\\end{table}",
        "",
    ]
    path.write_text("\n".join(lines))


def collect_metadata(command: list[str], returncode: int, stderr: str) -> dict[str, object]:
    return {
        "collected_at_utc": datetime.now(timezone.utc).isoformat(),
        "command": command,
        "system": platform.platform(),
        "cpu": run_text(["sysctl", "-n", "machdep.cpu.brand_string"]),
        "logical_cpus": run_text(["sysctl", "-n", "hw.logicalcpu"]),
        "memory_bytes": run_text(["sysctl", "-n", "hw.memsize"]),
        "bitcoinpir_git_rev": run_text(["git", "rev-parse", "HEAD"], cwd=DEFAULT_RUNTIME_REPO),
        "whitepaper_git_rev": run_text(["git", "rev-parse", "HEAD"]),
        "returncode": returncode,
        "stderr_tail": "\n".join(stderr.splitlines()[-80:]),
    }


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--data-root", type=Path, default=Path("/Volumes/Bitcoin/data"))
    parser.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR)
    parser.add_argument("--trials", type=int, default=5)
    parser.add_argument("--warmups", type=int, default=1)
    parser.add_argument("--database", default="all")
    parser.add_argument("--level", choices=["all", "index", "chunk"], default="all")
    parser.add_argument(
        "--summarize-only",
        action="store_true",
        help="Regenerate summary CSV/TeX from existing dpf_round_latency_trials.csv.",
    )
    args = parser.parse_args()

    args.out_dir.mkdir(parents=True, exist_ok=True)
    trials_path = args.out_dir / "dpf_round_latency_trials.csv"

    if args.summarize_only:
        with trials_path.open(newline="") as f:
            trial_rows = list(csv.DictReader(f))
    else:
        command = [
            "cargo",
            "run",
            "--release",
            "--manifest-path",
            str(BENCH_DIR / "Cargo.toml"),
            "--",
            "--data-root",
            str(args.data_root),
            "--trials",
            str(args.trials),
            "--warmups",
            str(args.warmups),
            "--database",
            args.database,
            "--level",
            args.level,
        ]
        env = os.environ.copy()
        env["CARGO_TARGET_DIR"] = str(Path("benchmarks/.dpf_target").resolve())
        completed = subprocess.run(command, text=True, capture_output=True, env=env, check=False)
        if completed.returncode != 0:
            print(completed.stderr)
            raise SystemExit(completed.returncode)
        trials_path.write_text(completed.stdout)
        (args.out_dir / "dpf_round_latency_environment.json").write_text(
            json.dumps(collect_metadata(command, completed.returncode, completed.stderr), indent=2)
            + "\n"
        )
        with trials_path.open(newline="") as f:
            trial_rows = list(csv.DictReader(f))

    summary = summarize(trial_rows)
    write_csv(args.out_dir / "dpf_round_latency_summary.csv", summary)
    write_tex(args.out_dir / "dpf_round_latency_table.tex", summary)
    print(f"Wrote {trials_path}")
    print(f"Wrote {args.out_dir / 'dpf_round_latency_summary.csv'}")
    print(f"Wrote {args.out_dir / 'dpf_round_latency_table.tex'}")


if __name__ == "__main__":
    main()
