#!/usr/bin/env python3
"""Run real-artifact HarmonyPIR offline hint-generation benchmarks.

This benchmark mirrors the runtime hint-pool phase: for a fixed PRP key it
generates all per-group HarmonyPIR hint frames for each selected INDEX/CHUNK
cuckoo artifact. It measures the offline state-preparation cost, not the online
query phase or a networked hint download.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import platform
import statistics
import subprocess
from datetime import datetime, timezone
from pathlib import Path


BENCH_DIR = Path("benchmarks/harmony_hint_bench")
DEFAULT_OUT_DIR = Path("benchmarks/results")
DEFAULT_RUNTIME_REPO = Path("/Users/cusgadmin/BitcoinPIR")


def run_text(cmd: list[str], cwd: Path | None = None) -> str:
    try:
        return subprocess.check_output(cmd, cwd=cwd, text=True).strip()
    except Exception:
        return ""


def percentile(values: list[float], pct: float) -> float:
    ordered = sorted(values)
    if len(ordered) == 1:
        return ordered[0]
    rank = (len(ordered) - 1) * pct
    lo = int(rank)
    hi = min(lo + 1, len(ordered) - 1)
    frac = rank - lo
    return ordered[lo] * (1 - frac) + ordered[hi] * frac


def write_csv(path: Path, rows: list[dict[str, object]]) -> None:
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()), lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)


def human_bytes(n: int) -> str:
    if n >= 1024**3:
        return f"{n / 1024**3:.2f} GiB"
    if n >= 1024**2:
        return f"{n / 1024**2:.1f} MiB"
    if n >= 1024:
        return f"{n / 1024:.1f} KiB"
    return f"{n} B"


def summarize(rows: list[dict[str, str]]) -> list[dict[str, object]]:
    groups: dict[tuple[str, str, str], list[dict[str, str]]] = {}
    for row in rows:
        groups.setdefault((row["database"], row["level"], row["backend"]), []).append(row)

    out: list[dict[str, object]] = []
    for (database, level, backend), group in groups.items():
        times = [float(r["hint_generation_s"]) for r in group]
        out.append(
            {
                "database": database,
                "level": level,
                "backend": backend,
                "trials": len(group),
                "k": group[0]["k"],
                "bins_per_table": group[0]["bins_per_table"],
                "padded_n": group[0]["padded_n"],
                "t": group[0]["t"],
                "m": group[0]["m"],
                "max_queries": group[0]["max_queries"],
                "entry_size_bytes": group[0]["entry_size_bytes"],
                "hints_bytes_total": group[0]["hints_bytes_total"],
                "request_bytes_per_query": group[0]["request_bytes_per_query"],
                "response_bytes_per_query": group[0]["response_bytes_per_query"],
                "median_hint_generation_s": f"{statistics.median(times):.6f}",
                "p95_hint_generation_s": f"{percentile(times, 0.95):.6f}",
            }
        )
    return out


def write_tex(path: Path, rows: list[dict[str, object]]) -> None:
    lines = [
        "% Auto-generated by benchmarks/benchmark_harmony_hint_latency.py.",
        "\\begin{table}[ht]",
        "\\centering",
        "\\scriptsize",
        "\\resizebox{\\linewidth}{!}{%",
        "\\begin{tabular}{lllrrrrr}",
        "\\hline",
        "Database & Level & Backend & Hints & Trials & Hint p50 & Hint p95 & Req/group \\\\",
        "\\hline",
    ]
    for r in rows:
        db = str(r["database"]).replace("_", "\\_")
        level = "INDEX" if r["level"] == "index" else "CHUNK"
        backend = "FastPRP" if r["backend"] == "fastprp" else "HMR12"
        lines.append(
            f"{db} & {level} & {backend} & {human_bytes(int(r['hints_bytes_total']))} & "
            f"{r['trials']} & {float(r['median_hint_generation_s']):.3f} s & "
            f"{float(r['p95_hint_generation_s']):.3f} s & "
            f"{human_bytes(int(r['request_bytes_per_query']))} \\\\"
        )
    lines += [
        "\\hline",
        "\\end{tabular}",
        "}%",
        "\\caption{Pilot local warm-cache HarmonyPIR offline hint-generation benchmark over current real cuckoo artifacts. Each row generates one complete set of per-group hints for the selected artifact and PRP backend. This measures offline client-state preparation, not online query latency or networked hint download.}",
        "\\label{tab:harmony-hint-benchmark}",
        "\\end{table}",
        "",
    ]
    path.write_text("\n".join(lines))


def collect_metadata(command: list[str], returncode: int, stderr: str) -> dict[str, object]:
    return {
        "collected_at_utc": datetime.now(timezone.utc).isoformat(),
        "command": command,
        "system": platform.platform(),
        "cpu": run_text(["sysctl", "-n", "machdep.cpu.brand_string"]),
        "logical_cpus": run_text(["sysctl", "-n", "hw.logicalcpu"]),
        "memory_bytes": run_text(["sysctl", "-n", "hw.memsize"]),
        "bitcoinpir_git_rev": run_text(["git", "rev-parse", "HEAD"], cwd=DEFAULT_RUNTIME_REPO),
        "whitepaper_git_rev": run_text(["git", "rev-parse", "HEAD"]),
        "returncode": returncode,
        "stderr_tail": "\n".join(stderr.splitlines()[-80:]),
    }


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--data-root", type=Path, default=Path("/Volumes/Bitcoin/data"))
    parser.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR)
    parser.add_argument("--trials", type=int, default=3)
    parser.add_argument("--warmups", type=int, default=1)
    parser.add_argument("--database", default="all")
    parser.add_argument("--level", choices=["all", "index", "chunk"], default="all")
    parser.add_argument("--backend", choices=["all", "hmr12", "fastprp"], default="all")
    parser.add_argument(
        "--summarize-only",
        action="store_true",
        help="Regenerate summary CSV/TeX from existing harmony_hint_latency_trials.csv.",
    )
    args = parser.parse_args()

    args.out_dir.mkdir(parents=True, exist_ok=True)
    trials_path = args.out_dir / "harmony_hint_latency_trials.csv"

    if args.summarize_only:
        with trials_path.open(newline="") as f:
            trial_rows = list(csv.DictReader(f))
    else:
        command = [
            "cargo",
            "run",
            "--release",
            "--manifest-path",
            str(BENCH_DIR / "Cargo.toml"),
            "--",
            "--data-root",
            str(args.data_root),
            "--trials",
            str(args.trials),
            "--warmups",
            str(args.warmups),
            "--database",
            args.database,
            "--level",
            args.level,
            "--backend",
            args.backend,
        ]
        env = os.environ.copy()
        env["CARGO_TARGET_DIR"] = str(Path("benchmarks/.harmony_target").resolve())
        completed = subprocess.run(command, text=True, capture_output=True, env=env, check=False)
        if completed.returncode != 0:
            print(completed.stderr)
            raise SystemExit(completed.returncode)
        trials_path.write_text(completed.stdout)
        (args.out_dir / "harmony_hint_latency_environment.json").write_text(
            json.dumps(collect_metadata(command, completed.returncode, completed.stderr), indent=2)
            + "\n"
        )
        with trials_path.open(newline="") as f:
            trial_rows = list(csv.DictReader(f))

    summary = summarize(trial_rows)
    write_csv(args.out_dir / "harmony_hint_latency_summary.csv", summary)
    write_tex(args.out_dir / "harmony_hint_latency_table.tex", summary)
    print(f"Wrote {trials_path}")
    print(f"Wrote {args.out_dir / 'harmony_hint_latency_summary.csv'}")
    print(f"Wrote {args.out_dir / 'harmony_hint_latency_table.tex'}")


if __name__ == "__main__":
    main()
