#!/usr/bin/env python3
"""Run pilot local scan benchmarks over Bitcoin PIR cuckoo artifacts.

The DPF/Harmony server path is dominated by scanning large cuckoo tables.
This benchmark is deliberately narrower than a full protocol benchmark: it
measures warm-cache sequential scan throughput for the current INDEX and CHUNK
cuckoo files at several scales, emits one CSV row per trial, and generates a
small TeX summary table for the whitepaper.
"""

from __future__ import annotations

import argparse
import csv
import statistics
import subprocess
from pathlib import Path
from typing import Iterable

from collect_artifact_metrics import (
    CHUNK_SLOT_SIZE,
    INDEX_SLOT_SIZE,
    human_bytes,
    metric_specs,
    parse_cuckoo_header,
)


SCANNER_SRC = Path(__file__).with_name("scan_file.rs")
SCANNER_BIN = Path(__file__).with_name(".scan_file_bin")


def compile_scanner() -> None:
    subprocess.run(
        ["rustc", "-O", str(SCANNER_SRC), "-o", str(SCANNER_BIN)],
        check=True,
    )


def run_scan(path: Path, offset: int, byte_count: int) -> tuple[int, float, str]:
    out = subprocess.check_output(
        [str(SCANNER_BIN), str(path), str(offset), str(byte_count)],
        text=True,
    ).strip()
    got, elapsed, checksum = out.split(",")
    return int(got), float(elapsed), checksum


def percentile(values: list[float], pct: float) -> float:
    if not values:
        return float("nan")
    ordered = sorted(values)
    if len(ordered) == 1:
        return ordered[0]
    rank = (len(ordered) - 1) * pct
    lo = int(rank)
    hi = min(lo + 1, len(ordered) - 1)
    frac = rank - lo
    return ordered[lo] * (1 - frac) + ordered[hi] * frac


def selected_artifacts(data_root: Path) -> Iterable[dict[str, object]]:
    for spec in metric_specs(data_root):
        for level, filename, slot_size in [
            ("index", "batch_pir_cuckoo.bin", INDEX_SLOT_SIZE),
            ("chunk", "chunk_pir_cuckoo.bin", CHUNK_SLOT_SIZE),
        ]:
            path = spec.artifact_dir / filename
            if not path.exists():
                continue
            header = parse_cuckoo_header(path)
            body_bytes = path.stat().st_size - (40 if level == "index" else 32)
            yield {
                "database": spec.name,
                "kind": spec.kind,
                "level": level,
                "path": path,
                "offset": 40 if level == "index" else 32,
                "bytes": body_bytes,
                "k": header.k,
                "bins_per_table": header.bins_per_table,
                "result_bytes": header.slots_per_bin * slot_size,
            }


def write_csv(path: Path, rows: list[dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)


def summarize(rows: list[dict[str, object]]) -> list[dict[str, object]]:
    groups: dict[tuple[str, str], list[dict[str, object]]] = {}
    for row in rows:
        groups.setdefault((str(row["database"]), str(row["level"])), []).append(row)

    out: list[dict[str, object]] = []
    for (database, level), group in groups.items():
        seconds = [float(r["elapsed_s"]) for r in group]
        gib_s = [float(r["throughput_gib_s"]) for r in group]
        byte_count = int(group[0]["bytes"])
        out.append(
            {
                "database": database,
                "level": level,
                "trials": len(group),
                "bytes": byte_count,
                "median_elapsed_s": f"{statistics.median(seconds):.6f}",
                "p95_elapsed_s": f"{percentile(seconds, 0.95):.6f}",
                "median_throughput_gib_s": f"{statistics.median(gib_s):.3f}",
                "p05_throughput_gib_s": f"{percentile(gib_s, 0.05):.3f}",
            }
        )
    return out


def write_tex(path: Path, rows: list[dict[str, object]]) -> None:
    lines = [
        "% Auto-generated by benchmarks/benchmark_scan_latency.py.",
        "\\begin{table}[ht]",
        "\\centering",
        "\\small",
        "\\begin{tabular}{llrrrrr}",
        "\\hline",
        "Database & Level & Scanned & Trials & Median & p95 & Median throughput \\\\",
        "\\hline",
    ]
    for r in rows:
        db = str(r["database"]).replace("_", "\\_")
        level = "INDEX" if r["level"] == "index" else "CHUNK"
        lines.append(
            f"{db} & {level} & {human_bytes(int(r['bytes']))} & {r['trials']} & "
            f"{float(r['median_elapsed_s']):.3f} s & {float(r['p95_elapsed_s']):.3f} s & "
            f"{float(r['median_throughput_gib_s']):.2f} GiB/s \\\\"
        )
    lines += [
        "\\hline",
        "\\end{tabular}",
        "\\caption{Pilot local warm-cache scan benchmark over current cuckoo artifacts. This measures sequential table-scan throughput, not end-to-end protocol latency.}",
        "\\label{tab:scan-benchmark}",
        "\\end{table}",
        "",
    ]
    path.write_text("\n".join(lines))


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--data-root", type=Path, default=Path("/Volumes/Bitcoin/data"))
    parser.add_argument("--out-dir", type=Path, default=Path("benchmarks/results"))
    parser.add_argument("--trials", type=int, default=3)
    parser.add_argument("--warmups", type=int, default=1)
    parser.add_argument(
        "--summarize-only",
        action="store_true",
        help="Regenerate summary CSV/TeX from existing scan_latency_trials.csv.",
    )
    args = parser.parse_args()

    args.out_dir.mkdir(parents=True, exist_ok=True)

    trial_rows: list[dict[str, object]] = []
    trials_path = args.out_dir / "scan_latency_trials.csv"
    if args.summarize_only:
        with trials_path.open(newline="") as f:
            trial_rows = list(csv.DictReader(f))
    else:
        compile_scanner()
        for artifact in selected_artifacts(args.data_root):
            path = artifact["path"]
            offset = int(artifact["offset"])
            byte_count = int(artifact["bytes"])
            print(f"{artifact['database']} {artifact['level']}: scanning {human_bytes(byte_count)}")

            for _ in range(args.warmups):
                run_scan(path, offset, byte_count)

            for trial in range(1, args.trials + 1):
                got, elapsed, checksum = run_scan(path, offset, byte_count)
                throughput = got / 1024**3 / elapsed
                row = {
                    "database": artifact["database"],
                    "kind": artifact["kind"],
                    "level": artifact["level"],
                    "trial": trial,
                    "bytes": got,
                    "elapsed_s": f"{elapsed:.9f}",
                    "throughput_gib_s": f"{throughput:.6f}",
                    "checksum": checksum,
                    "k": artifact["k"],
                    "bins_per_table": artifact["bins_per_table"],
                    "result_bytes": artifact["result_bytes"],
                    "path": path,
                }
                trial_rows.append(row)
                print(f"  trial {trial}: {elapsed:.3f}s, {throughput:.2f} GiB/s")
        write_csv(trials_path, trial_rows)

    summary = summarize(trial_rows)
    write_csv(args.out_dir / "scan_latency_summary.csv", summary)
    write_tex(args.out_dir / "scan_latency_table.tex", summary)
    print(f"Wrote {args.out_dir / 'scan_latency_trials.csv'}")
    print(f"Wrote {args.out_dir / 'scan_latency_summary.csv'}")
    print(f"Wrote {args.out_dir / 'scan_latency_table.tex'}")


if __name__ == "__main__":
    main()
