#!/usr/bin/env python3
"""Run real-artifact bucket-Merkle proof benchmarks.

The Rust runner verifies one randomized bin proof per PBC group. For each
sibling level it generates one DPF key per group, evaluates both local server
shares against the stored sibling tables, XORs the responses, and walks the
client hash path through the tree-top cache.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import platform
import statistics
import subprocess
from datetime import datetime, timezone
from pathlib import Path


BENCH_DIR = Path("benchmarks/merkle_round_bench")
DEFAULT_OUT_DIR = Path("benchmarks/results")
DEFAULT_RUNTIME_REPO = Path("/Users/cusgadmin/BitcoinPIR")


def run_text(cmd: list[str], cwd: Path | None = None) -> str:
    try:
        return subprocess.check_output(cmd, cwd=cwd, text=True).strip()
    except Exception:
        return ""


def percentile(values: list[float], pct: float) -> float:
    ordered = sorted(values)
    if len(ordered) == 1:
        return ordered[0]
    rank = (len(ordered) - 1) * pct
    lo = int(rank)
    hi = min(lo + 1, len(ordered) - 1)
    frac = rank - lo
    return ordered[lo] * (1 - frac) + ordered[hi] * frac


def write_csv(path: Path, rows: list[dict[str, object]]) -> None:
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()), lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)


def human_bytes(n: int) -> str:
    if n >= 1024**3:
        return f"{n / 1024**3:.2f} GiB"
    if n >= 1024**2:
        return f"{n / 1024**2:.1f} MiB"
    if n >= 1024:
        return f"{n / 1024:.1f} KiB"
    return f"{n} B"


def summarize(rows: list[dict[str, str]]) -> list[dict[str, object]]:
    groups: dict[tuple[str, str], list[dict[str, str]]] = {}
    for row in rows:
        groups.setdefault((row["database"], row["level"]), []).append(row)

    out: list[dict[str, object]] = []
    for (database, level), group in groups.items():
        keygen = [float(r["keygen_s"]) for r in group]
        server = [float(r["server_parallel_s"]) for r in group]
        client = [float(r["client_verify_s"]) for r in group]
        total = [float(r["total_parallel_s"]) for r in group]
        failures = sum(int(r["verification_failures"]) for r in group)
        out.append(
            {
                "database": database,
                "level": level,
                "trials": len(group),
                "k": group[0]["k"],
                "bins_per_table": group[0]["bins_per_table"],
                "arity": group[0]["arity"],
                "sibling_levels": group[0]["sibling_levels"],
                "tree_top_bytes": group[0]["tree_top_bytes"],
                "sibling_bytes_total": group[0]["sibling_bytes_total"],
                "upload_per_server_bytes": group[0]["upload_per_server_bytes"],
                "download_per_server_bytes": group[0]["download_per_server_bytes"],
                "verified_groups": sum(int(r["verified_groups"]) for r in group),
                "verification_failures": failures,
                "median_keygen_s": f"{statistics.median(keygen):.6f}",
                "median_server_parallel_s": f"{statistics.median(server):.6f}",
                "p95_server_parallel_s": f"{percentile(server, 0.95):.6f}",
                "median_client_verify_s": f"{statistics.median(client):.6f}",
                "p95_client_verify_s": f"{percentile(client, 0.95):.6f}",
                "median_total_parallel_s": f"{statistics.median(total):.6f}",
                "p95_total_parallel_s": f"{percentile(total, 0.95):.6f}",
            }
        )
    return out


def write_tex(path: Path, rows: list[dict[str, object]]) -> None:
    lines = [
        "% Auto-generated by benchmarks/benchmark_merkle_latency.py.",
        "\\begin{table}[ht]",
        "\\centering",
        "\\scriptsize",
        "\\resizebox{\\linewidth}{!}{%",
        "\\begin{tabular}{llrrrrrrrr}",
        "\\hline",
        "Database & Level & Levels & Trials & Upload/server & Download/server & Server p50 & Server p95 & Verify p50 & Total p50 \\\\",
        "\\hline",
    ]
    for r in rows:
        db = str(r["database"]).replace("_", "\\_")
        level = "INDEX" if r["level"] == "index" else "CHUNK"
        lines.append(
            f"{db} & {level} & {r['sibling_levels']} & {r['trials']} & "
            f"{human_bytes(int(r['upload_per_server_bytes']))} & "
            f"{human_bytes(int(r['download_per_server_bytes']))} & "
            f"{float(r['median_server_parallel_s']):.3f} s & "
            f"{float(r['p95_server_parallel_s']):.3f} s & "
            f"{float(r['median_client_verify_s']):.3f} s & "
            f"{float(r['median_total_parallel_s']):.3f} s \\\\"
        )
    lines += [
        "\\hline",
        "\\end{tabular}",
        "}%",
        "\\caption{Local warm-cache bucket-Merkle proof benchmark over current real artifacts. Each trial verifies one randomized bin proof per PBC group, using DPF sibling-table retrieval for each stored Merkle level and a client hash walk through the tree-top cache. The total is the local parallel critical path without WebSocket framing or network latency.}",
        "\\label{tab:merkle-proof-benchmark}",
        "\\end{table}",
        "",
    ]
    path.write_text("\n".join(lines))


def collect_metadata(command: list[str], returncode: int, stderr: str) -> dict[str, object]:
    return {
        "collected_at_utc": datetime.now(timezone.utc).isoformat(),
        "command": command,
        "system": platform.platform(),
        "cpu": run_text(["sysctl", "-n", "machdep.cpu.brand_string"]),
        "logical_cpus": run_text(["sysctl", "-n", "hw.logicalcpu"]),
        "memory_bytes": run_text(["sysctl", "-n", "hw.memsize"]),
        "bitcoinpir_git_rev": run_text(["git", "rev-parse", "HEAD"], cwd=DEFAULT_RUNTIME_REPO),
        "whitepaper_git_rev": run_text(["git", "rev-parse", "HEAD"]),
        "returncode": returncode,
        "stderr_tail": "\n".join(stderr.splitlines()[-80:]),
    }


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--data-root", type=Path, default=Path("/Volumes/Bitcoin/data"))
    parser.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR)
    parser.add_argument("--trials", type=int, default=30)
    parser.add_argument("--warmups", type=int, default=1)
    parser.add_argument("--database", default="all")
    parser.add_argument("--level", choices=["all", "index", "chunk"], default="all")
    parser.add_argument(
        "--summarize-only",
        action="store_true",
        help="Regenerate summary CSV/TeX from existing merkle_latency_trials.csv.",
    )
    args = parser.parse_args()

    args.out_dir.mkdir(parents=True, exist_ok=True)
    trials_path = args.out_dir / "merkle_latency_trials.csv"

    if args.summarize_only:
        with trials_path.open(newline="") as f:
            trial_rows = list(csv.DictReader(f))
    else:
        command = [
            "cargo",
            "run",
            "--release",
            "--manifest-path",
            str(BENCH_DIR / "Cargo.toml"),
            "--",
            "--data-root",
            str(args.data_root),
            "--trials",
            str(args.trials),
            "--warmups",
            str(args.warmups),
            "--database",
            args.database,
            "--level",
            args.level,
        ]
        env = os.environ.copy()
        env["CARGO_TARGET_DIR"] = str(Path("benchmarks/.merkle_target").resolve())
        completed = subprocess.run(command, text=True, capture_output=True, env=env, check=False)
        if completed.returncode != 0:
            print(completed.stderr)
            raise SystemExit(completed.returncode)
        trials_path.write_text(completed.stdout)
        (args.out_dir / "merkle_latency_environment.json").write_text(
            json.dumps(collect_metadata(command, completed.returncode, completed.stderr), indent=2)
            + "\n"
        )
        with trials_path.open(newline="") as f:
            trial_rows = list(csv.DictReader(f))

    if not trial_rows:
        raise SystemExit(f"no trial rows found in {trials_path}")
    failure_count = sum(int(row["verification_failures"]) for row in trial_rows)
    if failure_count:
        raise SystemExit(f"Merkle benchmark recorded {failure_count} failures")

    summary = summarize(trial_rows)
    write_csv(args.out_dir / "merkle_latency_summary.csv", summary)
    write_tex(args.out_dir / "merkle_latency_table.tex", summary)
    print(f"Wrote {trials_path}")
    print(f"Wrote {args.out_dir / 'merkle_latency_summary.csv'}")
    print(f"Wrote {args.out_dir / 'merkle_latency_table.tex'}")


if __name__ == "__main__":
    main()
